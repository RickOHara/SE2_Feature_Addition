    @SuppressWarnings("serial")
    class ImagePanel extends JPanel
    {
        public ImagePanel ()
        {
            setSize(400,400);
            setVisible(true);

            addMouseListener(new MouseListener()
            {
                @Override
                public void mouseClicked(MouseEvent e){}

                @Override
                public void mousePressed(MouseEvent e)
                {
                    selectAll.setSelected(false);
                    startPos = new Point(e.getX(),e.getY());
                    endPos = new Point(e.getX(),e.getY());
                }

                @Override
                public void mouseReleased(MouseEvent e)
                {
                    selectAll.setSelected(false);
                    endPos = new Point(e.getX(),e.getY());
                    if (endPos.x < startPos.x)
                    {
                        int temp = endPos.x;
                        endPos.x = startPos.x;
                        startPos.x = temp;
                    }
                    if (endPos.y < startPos.y)
                    {
                        int temp = endPos.y;
                        endPos.y = startPos.y;
                        startPos.y = temp;
                    }
                }

                @Override
                public void mouseEntered(MouseEvent e){}

                @Override
                public void mouseExited(MouseEvent e){}
            });
            
            addMouseMotionListener(new MouseMotionListener()
            {

                @Override
                public void mouseDragged(MouseEvent e)
                {
                    selectAll.setSelected(false);
                    endPos = new Point(e.getX(),e.getY());
                }

                @Override
                public void mouseMoved(MouseEvent e) {}
                
            });

            addMouseMotionListener(new MouseMotionListener()
            {
                @Override
                public void mouseDragged(MouseEvent e){}

                @Override
                public void mouseMoved(MouseEvent e){}                
            });


            Timer repaint = new Timer(50, new PaintTimer());
            repaint.start();
        }


        class PaintTimer implements ActionListener
        {
            @Override
            public void actionPerformed(ActionEvent e)
            {
                repaint();
            }
        }



        protected void paintComponent (Graphics g)
        {
            super.paintComponent(g);

            setBackground(Color.black);

            if (image != null)
            {
                this.setSize(image.getWidth(),image.getHeight());
                g.drawImage(image,0,0,null);
            }

            g.setColor(Color.red);
            if (endPos.x < startPos.x)
            {
                if (endPos.y < startPos.y)
                    g.drawRect(endPos.x, endPos.y, Math.abs(endPos.x-startPos.x), Math.abs(endPos.y-startPos.y));
                else
                    g.drawRect(endPos.x, startPos.y, Math.abs(endPos.x-startPos.x), Math.abs(endPos.y-startPos.y));
            }
            else if (endPos.y < startPos.y)
                g.drawRect(startPos.x, endPos.y, Math.abs(endPos.x-startPos.x), Math.abs(endPos.y-startPos.y));
            else
                g.drawRect(startPos.x, startPos.y, Math.abs(endPos.x-startPos.x), Math.abs(endPos.y-startPos.y));
        }

    }



