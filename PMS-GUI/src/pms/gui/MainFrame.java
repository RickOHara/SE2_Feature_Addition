    private void choosePictureActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_choosePictureActionPerformed
        // Get bmp file 
        if (evt.getSource() == choosePicture) {
            //filter that ensures only bmp files are selected
            fc.setFileFilter(new BmpFileFilter());
            int returnVal = fc.showOpenDialog(null);

            try {
                if (returnVal == JFileChooser.APPROVE_OPTION) {
                    chosenFile = fc.getSelectedFile();
                    originalFile = chosenFile;
                    //Display image in GUI
                    Dimension dm = new Dimension(picLabel.getHeight(), picLabel.getWidth());
                    picLabel.setPreferredSize(dm);
                    setImage(chosenFile);

                } else {
                    JOptionPane.showMessageDialog(null, JOptionPane.WARNING_MESSAGE);

                }
            } catch (Exception e) {//Catch exception if any
                System.err.println("Error: " + e.getMessage());
            }
        }

        //string location saved to be written to file for ACL2 to reference 
        pictureLocation = chosenFile.getPath();
        int index = pictureLocation.indexOf(".");
        NEWFILEPATH = pictureLocation.substring(0, index);


    }//GEN-LAST:event_choosePictureActionPerformed

