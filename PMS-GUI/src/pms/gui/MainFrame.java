    private void filterExitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_filterExitActionPerformed

        ColorStringToNum cs = new ColorStringToNum();
        filter = filterColorChooser.getColor();
        String values = cs.colorToNum(filter.toString(), 1);
        //x/100 = y/255, solve for y to get values in range of 255
        filterValue = (int) (filterScale.getValue() * 0.01 * 255);

        try {
            // Create file 
            FileWriter fstream = new FileWriter("pmsInfo.txt");
            BufferedWriter out = new BufferedWriter(fstream);
            out.write(pictureLocation + " 16 " + values + " " + filterValue);
            selectedValue = 16;
            //Close the output stream
            out.close();
        } catch (Exception e) {//Catch exception if any
            System.err.println("Error: " + e.getMessage());
        }

        //Close frame
        filterFrame.dispose();



    }//GEN-LAST:event_filterExitActionPerformed

